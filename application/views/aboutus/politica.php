                            <h2>POLÍTICA INTEGRADA DE SOUTH TRADE SHIPMANAGEMENT</h2>
                            <p>&nbsp;</p>
                            <p>El objetivo de esta política es disminuir el riesgo y evitar el daño a las personas,
                                bienes y el medioambiente con el fin de proporcionar un servicio sostenible y libre de
                                incidentes mediante un cuidado responsable.</p>
                            <p>El Sistema de Gestión Integrada QHSE (Calidad, Salud, Seguridad, Protección,
                                Medioambiente y Eficiencia Energética) cumple con los códigos IGS, ISPS, las normas ISO
                                9001, ISO 14001, ISO 45001, ISO 50001, los requerimientos legales y otros
                                requerimientos, normas y directrices relevantes de la industria. El Sistema de Gestión
                                Integrada se revisa periódicamente para evaluar en general su efectividad en el alcance
                                de los objetivos propuestos.</p>
                            <p>Esta política abarca:</p>
                            <ul>
                                <li>SEGURIDAD: La empresa está comprometida con la identificación de todos los riesgos
                                    asociados a la operación de naves. Del mismo modo, aplica todas las garantías
                                    necesarias para proporcionar un lugar de trabajo seguro junto a prácticas seguras en
                                    términos de la salud y seguridad de sus empleados y de terceros. Mediante este
                                    compromiso de la mejora continua del Sistema de Gestión Integrada de South Trade
                                    Shipmanagement y del desarrollo de capacidades de gestión de seguridad de su
                                    personal, en tierra y a bordo, la empresa busca lograr el objetivo a largo plazo de
                                    ningún accidente para todas las personas que trabajan en la flota.</li>
                                <li>CALIDAD: La empresa está comprometida con el logro de los requerimientos de calidad
                                    de sus clientes al garantizar que los servicios cumplirán las normas de éstos según
                                    el Sistema de Gestión Integrada de South Trade Shipmanagement.</li>
                                <li>SALUD: La empresa proporcionará condiciones de trabajo seguras y saludables para la
                                    prevención de lesiones y deterioro de la salud de su personal en tierra y abordo.
                                    También se compromete a incluir la participación y consulta de los trabajadores o
                                    sus representantes. La empresa reconoce la importancia del bienestar y fomenta el
                                    compromiso de los empleados de mantener altos estándares de salud personal, limpieza
                                    e higiene.</li>
                                <li>MEDIOAMBIENTE: La empresa reconoce que una de sus mayores prioridades es la
                                    protección del medioambiente y que se debe hacer los mayores esfuerzos posibles para
                                    la conservación y protección del medioambiente marítimo y atmosférico y, además,
                                    hacerse cargo de cualquier otro tipo de desechos, ya sea de origen doméstico o de
                                    oficinas. Al adoptar la metodología de evaluación de riesgos en todos sus niveles de
                                    operación, la empresa se compromete a implementar los controles necesarios en cuanto
                                    a todas las emisiones y descargas contaminantes para disminuir impactos ambientales
                                    adversos.</li>
                                <li>ENERGÍA: La empresa está comprometida con la disminución de emisiones perjudiciales
                                    producidas por la quema de combustibles fósiles y a todas las naves se les ha
                                    proporcionado el Plan de Gestión de la Eficiencia Energética del Buque (PGEEB) con
                                    el fin de contribuir a la conservación de la energía.</li>
                                <li>PROTECCIÓN: La empresa cumple plenamente con el Código PBIP y, de esta forma, lucha
                                    por proporcionar un ambiente de trabajo seguro al establecer y mantener medidas de
                                    seguridad que previenen las actividades ilegales o acciones hostiles en contra de
                                    naves que supongan una amenaza y puedan poner en peligro la seguridad y protección
                                    de personas, propiedad y el medio ambiente.</li>
                                <li>DROGAS Y ALCOHOL: La empresa prohíbe estrictamente el consumo, la posesión y el
                                    contrabando de drogas y alcohol en cualquiera de las naves y en recintos de la
                                    empresa. En el caso de que algún empleado contravenga esta política, será sometido a
                                    los procedimientos disciplinarios estipulados en el Sistema de Gestión Integrada.
                                </li>
                            </ul>
